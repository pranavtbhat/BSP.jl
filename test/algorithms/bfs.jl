# Single seed test
gstruct = Vector{Int}[[2,3,4], [3,5,6], [4,6], [7], [6,8], [7,8], [8], Int[]]
@test bfs(gstruct) == ([0,1,1,1,2,2,2,3], [0,1,1,1,2,2,4,5])

# Multi seed test
gstruct = Vector{Int}[[2,3], [1,4], [1,4], [2,3], [6,7], [5,7], [5,6], [9,10], [8,10], [8,9,11], [10], [13], [12]]
@test bfs(gstruct, [1,5,8,12]) == ([0,1,1,2,0,1,1,0,1,1,2,0,1],[0,1,1,2,0,5,5,0,8,8,10,0,12])
